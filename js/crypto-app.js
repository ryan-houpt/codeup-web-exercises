// 100 btn Modal

// Get the modal
var oneHundredModal = document.getElementById('one-hundred-modal');

// Get the button that opens the modal
var oneHundredBtn = document.getElementById("one-hundred-btn");

// Get the <span> element that closes the modal
var oneHundredSpan = document.getElementsByClassName("one-hundred-close")[0];

// When the user clicks the button, open the modal
oneHundredBtn.onclick = function() {
    oneHundredModal.style.display = "block";
};

// When the user clicks on <span> (x), close the modal
oneHundredSpan.onclick = function() {
    oneHundredModal.style.display = "none";
};

// When the user clicks anywhere outside of the modal, close it
window.onclick = function(event) {
    if (event.target === oneHundredModal) {
        oneHundredModal.style.display = "none";
    }
};

